                <p><b>
                    <hr>

                    <h4>Condiciones a cumplir: </h4>
                    <p>El torneo de fútbol estará a cargo de LUEFI, entidad que nuclea clubes, sociedades de fomento, escuelas de fútbol, colegios y cualquier otro organismo que reúna los requisitos necesarios para participar. Estos, se ajustaran indefectiblemente al siguiente reglamento. Solo participarán en los campeonatos las instituciones invitadas y aprobadas por LUEFI. LUEFI se reserva el derecho de admisión y permanencia en el torneo. </p>


                    <h4>1 - Puntuación: </h4>
                    <p>-Se realizará una tabla de posiciones “General” y otra por “Categorías”. </p>

                    <p>-Se le otorgará la siguiente puntuación por resultado: </p>
                    <p>*Partido Ganado 3 Puntos</p>
                    <p>*Partido Empatado 2 Puntos</p>
                    <p>*Partido Perdido 1 Punto</p>
                    <p>*Partido No Presente 0 Punto</p>
                    <p>-La tabla General contará con los puntos de las siguientes categorías: 03/04-05/06-07/08 </p>

                    <p>-La categoría 97/98-01/02 y 09/10 no sumaran en la tabla general ya que hay escuelas que no cuentan con esas categorías, pero, si tendrán una tabla de posición individual como todas las demás. </p>

                    <h4>2 – Responsabilidades y obligaciones:</h4>
                    <p>-El equipo Local NO puede cobrar entrada a ningún alumno que participe del Torneo como así también a los adultos que acompañen a los participantes. (Estacionamiento queda a cargo de cada Club si quieren o no cobrar). </p>

                    <p>-El equipo Local tiene que contar con servicio médico de urgencia para todos los participantes.</p>

                    <p>-Al finalizar cada partido es de carácter obligatorio compartir el tercer tiempo, para fomentar la finalidad del Torneo. (El mismo será con gaseosas y galletitas o lo que se pongan en común acuerdo los 2 equipos que disputaron la fecha). </p>

                    <p>-El equipo Local por cualquier eventualidad de suspender jornada, deberá avisar con antelación al equipo Rival. </p>

                    <p>-El equipo Visitante tiene que avisar con antelación las categorías que va a llevar para participar de la fecha. </p>

                    <p>-Los equipos que juegan la jornada se tienen que poner en contacto para definir horarios, arbitro y tercer tiempo. </p>

                    <p>-Cada escuela es responsable de sus alumnos y padres, en función de la conducta de los mismos. </p>

                    <p>-No cumplir con las obligaciones de ítems anteriores, mala conducta deportiva, faltar a partidos programados en más de una ocasión derivan en la exclusión del torneo LUEFI. </p>


                    <h4>3 – Reglamento de juego:</h4>
                    <p>El cronograma y horario de partidos será a convenir entre los equipos que jueguen entre sí.</p>
                    <p>-Las dimensiones de cancha serán por categoría: <p>
                        <p>• Quinta – Categorías 97/98 - Cancha de 11</p>
                        <p>• Sexta – Categorías 01/02 - Cancha de 11</p>
                        <p>• Séptima – Categorías 03/04 - Cancha de 11</p>
                        <p>• Octava – Categorías 05/06 - Cancha de 9</p>
                        <p>• Novena – Categorías 06/07 - Cancha de 7</p>
                        <p>• Promocional – Categorías 90/10 – Cancha de 6 o 7</p>
                        <p>Nota: Los horarios y dimensiones de las canchas se podrán ver modificadas en común acuerdo entre los equipos que se enfrenten. </p>


                        <h4>4 - Consideraciones:</h4>
                        <p>-La Liga LUEFI contará con una Página Web donde se detallará una breve descripción de los equipos participantes con los datos de la ubicación de los predios, el fixture, la tabla de posiciones y galería con fotos de las jornadas. También servirá como medio de información del presente Reglamento, Fechas suspendidas u otros acontecimientos. </p>

                        <p>-Los equipos antes de jugar cada encuentro deberán presentar una planilla donde figuren los jugadores que forman parte del planten en el cual firmaran ante cada encuentro. </p>

                        <p>- Cada equipo deberá abonar desde el mes de Abril la suma de $300.- para el mantenimiento general de la Página Web. Este monto será abonado hasta el mes de Diciembre inclusive. </p>

                        <p>-Se recomienda utilizar el saludo FIFA al comienzo de cada partido y finalizado el mismo compartir el tercer tiempo. (Carácter obligatorio). </p>

                        <p>-Los gatos ocasionados en la jornada, por árbitros y tercer tiempo, serán repartidos por ambos equipos. </p>

                        <p>-Se deben enviar vía email las fotos que se tengan de la jornada para así poder compartirlas en la Página Web y en Facebook u otras redes sociales. La dirección de email para enviar las fotos es</p><a href="#">sociales@torneoluefi.com.ar</a>

                        <p>-Cada escuela cobrará en forma unilateral el costo de la participación de sus alumnos. Se acordó en reuniones un precio estimativo de $45.- </p>

                        <p>-Cuando el resultado sea superior a 6 goles, se buscaran recursos para emparejar el encuentro. Por ejemplo: </p>
                        <p>•Goles dentro del área</p>
                        <p>•Sumar un jugador al equipo rival</p>
                        <p>•Los profesores buscan recursos</p>
                        <p>Esto sirve para no perder el foco y diferenciarnos entre clubes profesionales y nosotros que estamos haciendo docencia. </p>

                        <p>Habiendo tomado conocimiento de todos los artículos del presente reglamento, dejamos constancia oficial de nuestra aceptación y conformidad, comprometiéndonos a cumplir y a hacer cumplir su letra y espíritu. </p>

                        <p>Este Reglamento debe ser considerado por los Coordinadores de los Clubes, los Profesores, los chicos que integren los planteles y también quienes los acompañen a los encuentros para así no juzgar sin tener conocimiento de las reglas pactadas para este Torneo. </p>


